import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        int n = 0;
        Scanner sc = new Scanner(System.in);
        while(sc.hasNext() == true){
            n++;
            System.out.printf("%d %s%n", n, sc.nextLine());
        }
    }
}
